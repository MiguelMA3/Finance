
public class Debt {
    String description;
    double preco;
    String payday;
    boolean recorrente;

    public Debt(String description, double preco, String payday, boolean recorrente) {
        this.description = description;
        this.preco = preco;
        this.payday = payday;
        this.recorrente = recorrente;
    }

    
}